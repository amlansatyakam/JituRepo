package com.amlan.bo;

public class EmployeeBO {
	private int eid;
	private String fname;
	private int salary;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	@Override
	public String toString() {
		return "EmployeeBO [eid=" + eid + ", fname=" + fname + ", salary=" + salary + "]";
	}

	
}
